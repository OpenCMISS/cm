                CASE DEFAULT
                  LOCAL_ERROR="The second equations set specification of "// &
                    & TRIM(NUMBER_TO_VSTRING(EQUATIONS_SET_SPEC_TYPE,"*",ERR,ERROR))// &
                    & " is invalid for a bioelectrics class."
                  CALL FlagError(LOCAL_ERROR,ERR,ERROR,*999)
