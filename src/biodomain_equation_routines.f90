                  CASE(EQUATIONS_SET_FIRST_BIDOMAIN_SUBTYPE)
                    CALL EQUATIONS_TIME_DEPENDENCE_TYPE_SET(EQUATIONS,EQUATIONS_FIRST_ORDER_DYNAMIC,ERR,ERROR,*999)
