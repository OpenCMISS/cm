        CASE(EQUATIONS_SET_SETUP_START_ACTION)
          IF(EQUATIONS_SET%DEPENDENT%DEPENDENT_FINISHED) THEN
            IF(ASSOCIATED(EQUATIONS_SET%MATERIALS)) THEN
              IF(EQUATIONS_SET%MATERIALS%MATERIALS_FINISHED) THEN
